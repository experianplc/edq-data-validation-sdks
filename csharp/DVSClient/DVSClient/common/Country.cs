        private static readonly Dictionary<string, Country> Iso3CodeMap = new Dictionary<string, Country>();

        static Country()
        {
            foreach (var country in GetAllCountries())
            {
                Iso3CodeMap[country.Iso3Code] = country;
            }
        }

        public static Country FromIso3(string iso3Code)
        {
            return Iso3CodeMap.TryGetValue(iso3Code, out var country) ? country : Unknown;
        }

        private Country(string name, string iso2Code, string iso3Code)
        {
            Name = name;
            Iso2Code = iso2Code;
            Iso3Code = iso3Code;
        }

